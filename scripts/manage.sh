# @function stop_workloads
# @command stop
# Stop all inference jobs and Ray workloads plus dev components.
# Safe to run before node reboot.

stop_workloads() {
  log "Stopping managed workloads (all models + ray)..."
  # Specific for test compatibility + labels for generality
  kubectl delete job kimi kimi-test nemotron-3-ultra glm-5.2 glm-5.2-rpc ray-head ray-worker -n "${NAMESPACE}" --ignore-not-found=true --grace-period=30 || true
  kubectl delete job -l workload=inference -n "${NAMESPACE}" --ignore-not-found=true --grace-period=30 || true
  kubectl delete job -l workload=ray -n "${NAMESPACE}" --ignore-not-found=true --grace-period=30 || true
  kubectl delete pod -l workload=inference -n "${NAMESPACE}" --ignore-not-found=true --grace-period=30 || true
  kubectl delete pod -l workload=ray -n "${NAMESPACE}" --ignore-not-found=true --grace-period=30 || true
  stop_dev_workloads || true
  log "Workloads stopped. Pods may still be terminating."
}

# @function cleanup
# @command cleanup
# Destructive namespace cleanup after interactive DELETE confirmation.
# Deletes ai-inference and dev namespace resources.

cleanup() {
  warn "This will delete ALL resources in namespace ${NAMESPACE} managed by this lab."
  read -r -p "Type 'DELETE' to confirm full cleanup: " confirm
  if [[ "$confirm" != "DELETE" ]]; then
    log "Cleanup aborted."
    exit 0
  fi

  stop_workloads
  kubectl delete svc kimi kimi-test nemotron-3-ultra glm-5.2 ray-head ray-worker -n "${NAMESPACE}" --ignore-not-found || true
  kubectl delete svc -l workload=inference -n "${NAMESPACE}" --ignore-not-found || true
  kubectl delete svc -l workload=ray -n "${NAMESPACE}" --ignore-not-found || true
  kubectl delete ns "${NAMESPACE}" --ignore-not-found || true
  # Dev ns (non-destructive on purpose; user can delete explicitly)
  for ns in coder kasm monitoring dev; do
    kubectl delete all,cm,secret,sa,role,rolebinding -n "$ns" --all --ignore-not-found || true
  done
  log "Cleanup complete."
}

