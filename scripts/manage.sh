# ## start-default / start-safe
# Automatically chooses a safe starting workload based on detected free GPUs.
# Falls back to the lightest test workload.
#
# @command start-default
# @command start-safe
# Usage:
#   ./scripts/manage.sh start-default
#
# Safety:
#   Calls start_test after capacity check. Never starts heavy models directly.
# @function start_default
# Auto-select safe test workload based on approximate free GPU count.
# Always delegates to start_test.

start_default() {
  require_kubectl
  check_cluster_access
  local free
  free=$(get_approx_free_gpus 2>/dev/null || echo "unknown")
  log "Free GPUs detected (approx): $free"
  if [[ "$free" != "unknown" && "$free" -lt 2 ]]; then
    warn "Low capacity; starting lightest safe workload."
  fi
  start_test
}

