# @function resources_cmd
# Resource Guard CLI: capacity, check, suggest, apply-policy.
resources_cmd() {
  local sub="${1:-status}"
  shift || true
  case "$sub" in
    status|""|capacity)
      print_resources_status
      ;;
    check)
      local action="${1:-}"
      local json_flag="${2:-}"
      if [[ -z "$action" ]]; then
        err "Usage: resources check <action> [--json]   e.g. model:kimi-test"
        exit 1
      fi
      if [[ "$json_flag" == "--json" ]]; then
        check_capacity "$action"
      else
        check_capacity "$action" | jq '.'
      fi
      ;;
    suggest)
      local action="${1:-}"
      if [[ -z "$action" ]]; then
        err "Usage: resources suggest <action>"
        exit 1
      fi
      suggest_free_resources "$action" | jq '.'
      ;;
    apply-policy)
      log "Applying Resource Guard manifests from k8s/base/resource-guard..."
      kubectl apply -k "${REPO_ROOT}/k8s/base/" || true
      log "Resource policy applied (PriorityClass, ResourceQuota, LimitRange, PDB)."
      ;;
    *)
      err "Unknown resources subcommand: $sub (try status, check, suggest, apply-policy)"
      exit 1
      ;;
  esac
}

