def render_grafana_dashboards(domains: dict[str, Any], root: Path) -> None:
    """Invoke the Grafana dashboard generator when the script is present.

    Args:
        domains: Resolved lab domain configuration (passed via env to the child).
        root: Repository root containing ``config/grafana/generate-dashboards.py``.

    Raises:
        subprocess.CalledProcessError: If the dashboard generator exits non-zero.
    """
    gen = root / "config" / "grafana" / "generate-dashboards.py"
    if not gen.exists():
        return
    import subprocess

    env = os.environ.copy()
    env["LAB_DOMAINS_CONFIG"] = str(root / "config" / "lab-domains.yaml")
    subprocess.run([sys.executable, str(gen)], check=True, cwd=root, env=env)


def render_cert_manager_kustomization(domains: dict[str, Any], root: Path) -> None:
    """Write ``k8s/cert-manager/kustomization.yaml`` listing generated cert resources.

    Args:
        domains: Resolved lab domain configuration (public certs when configured).
        root: Repository root path.
    """
    resources = ["cluster-issuer.yaml", "generated/wildcard-certificate.yaml"]
    if domains.get("public_domain"):
        resources.extend(
            [
                "generated/cluster-issuer-acme.yaml",
                "generated/public-wildcard-certificate.yaml",
            ]
        )
    lines = [
        "# Purpose: Kustomize bundle for cert-manager issuers and generated certificates",
        "# Source of truth: generated by render_domains.py from config/lab-domains.yaml",
        "# Regenerate: bazelisk run //scripts:run-utility -- render-domains",
        "# Safety: TLS material only; no cluster credentials",
        "",
        "apiVersion: kustomize.config.k8s.io/v1beta1",
        "kind: Kustomization",
        "",
        "resources:",
        *[f"  - {r}" for r in resources],
        "",
    ]
    (root / "k8s/cert-manager/kustomization.yaml").write_text("\n".join(lines))


def main() -> int:
    """Load domain config and render all domain-dependent generated manifests.

    Returns:
        ``0`` on success.
    """
    root = repo_root()
    domains_path = root / "config" / "lab-domains.yaml"
    sso_path = root / "config" / "sso-policy.yaml"
    domains = apply_env(load_yaml(domains_path))
    sso_policy = load_yaml(sso_path)

    sync_lab_domains_json(domains, root)
    render_routes(domains, sso_policy, root)
    render_authelia_configmap(domains, root)
    render_oauth2_proxy(domains, root)
    render_certificates(domains, root)
    render_cert_manager_kustomization(domains, root)
    render_ansible_values(domains, root)
    render_grafana_dashboards(domains, root)
    print(f"render-domains: ok (local={domains['local_domain']} public={domains['public_domain'] or '<none>'})")
    return 0


